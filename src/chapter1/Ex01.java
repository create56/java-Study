package chapter1;

// 클래스 : 소스 코드의 최소 단위
public class Ex01 {
	//메인함수 : 시작점 내가 작성하 소스코드의 시작이 어딘지 알려주는 기능
	public static void main(String[] args) {
		// 주석: 메모,개발자가  참고해야할 사항
		
		//무언가를 출력하기 위한 기능
		// () 안에다가 출력 대상을 지정
		System.out.println("HelloWorld~!");
		System.out.println("주석 연습 중...");		// Hello world 문자열 출력
		System.out.println("출력1");
		System.out.println("출력2");
		
		// System.out.println(); //소스코드를 주석처리하면 실행되지 않음
		System.out.println();
		System.out.println();
		System.out.println();
		System.out.println();
	}

}
