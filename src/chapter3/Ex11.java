public class Ex11 {

	public static void main(String[] args) {
		// i가 0부터
		int i = 0;
		// 9이하 일 동안
		while (i <= 9) {
			System.out.println(i);
			
			// 하나씩 증가하면서                                                                                                                                                                                                                                                                                                                                                                                      
			i = i+1;
		}

	}

}
